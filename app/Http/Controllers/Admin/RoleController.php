     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate(['role' => 'required']);

        $name = $request->input('role');
        $slug = str_slug($name);

        Role::create(['name' => $slug, 'title' => $name]);

        return back();
    }

